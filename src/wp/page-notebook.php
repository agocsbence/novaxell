                                            <h3 data-w-id="e62f9af1-26af-0eeb-785d-50f16b3040ea" class="entry-title entry-title-notebook">Tell one single coherent story</h3>
                                        </a>
                                        <div class="w-richtext">
                                            <p>
                                                You have so much data and so much to say. It is tempting to try to fit it all in. However, it will easily turn into a collection of unrelated random facts. That is not how you tell
                                                a good story. Resist the temptation and tell one single, focused story. Choose quality over quantity. One well-structured coherent story will be more memorable, more interesting
                                                and much more powerful.
                                            </p>
